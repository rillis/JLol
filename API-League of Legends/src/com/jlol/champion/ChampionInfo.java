package com.jlol.champion;

public class ChampionInfo {
	int attack;
	int defense;
	int magic;
	int difficulty;
	
	public ChampionInfo(int attack,	int defense, int magic, int difficulty) {
		this.attack = attack;
		this.defense = defense;
		this.magic = magic;
		this.difficulty = difficulty;
	}
}
